  integer ICLOCS_StirredTank::rhs_ode_numEqns() const { return 2; }

  void
  ICLOCS_StirredTank::rhs_ode_eval(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = P__[iP_TimeSize];
    real_type t2   = X__[iX_x1];
    real_type t5   = 1.0 / ModelPars[iM_theta];
    real_type t10  = X__[iX_x2];
    real_type t13  = exp(-1.0 / t10 * ModelPars[iM_En]);
    real_type t14  = t13 * t2 * ModelPars[iM_k];
    result__[ 0   ] = (t5 * (1 - t2) - t14) * t1;
    result__[ 1   ] = (t5 * (ModelPars[iM_Tf] - t10) + t14 - (t10 - ModelPars[iM_Tc]) * U__[iU_u] * ModelPars[iM_a]) * t1;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "rhs_ode", 2, i_segment );
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  integer ICLOCS_StirredTank::Drhs_odeDxup_numRows() const { return 2; }
  integer ICLOCS_StirredTank::Drhs_odeDxup_numCols() const { return 4; }
  integer ICLOCS_StirredTank::Drhs_odeDxup_nnz()     const { return 7; }

  void
  ICLOCS_StirredTank::Drhs_odeDxup_pattern( integer iIndex[], integer jIndex[] ) const {
    iIndex[0 ] = 0   ; jIndex[0 ] = 0   ;
    iIndex[1 ] = 0   ; jIndex[1 ] = 1   ;
    iIndex[2 ] = 0   ; jIndex[2 ] = 3   ;
    iIndex[3 ] = 1   ; jIndex[3 ] = 0   ;
    iIndex[4 ] = 1   ; jIndex[4 ] = 1   ;
    iIndex[5 ] = 1   ; jIndex[5 ] = 2   ;
    iIndex[6 ] = 1   ; jIndex[6 ] = 3   ;
  }


  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

  void
  ICLOCS_StirredTank::Drhs_odeDxup_sparse(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = P__[iP_TimeSize];
    real_type t3   = 1.0 / ModelPars[iM_theta];
    real_type t4   = ModelPars[iM_k];
    real_type t5   = ModelPars[iM_En];
    real_type t6   = X__[iX_x2];
    real_type t9   = exp(-1.0 / t6 * t5);
    result__[ 0   ] = (-t9 * t4 - t3) * t1;
    real_type t12  = t4 * t1;
    real_type t13  = X__[iX_x1];
    real_type t15  = t6 * t6;
    real_type t18  = t9 / t15 * t5;
    result__[ 1   ] = -t18 * t13 * t12;
    real_type t22  = t13 * t4;
    real_type t23  = t9 * t22;
    result__[ 2   ] = t3 * (1 - t13) - t23;
    result__[ 3   ] = t9 * t12;
    real_type t25  = ModelPars[iM_a];
    real_type t27  = U__[iU_u] * t25;
    result__[ 4   ] = (t18 * t22 - t27 - t3) * t1;
    real_type t31  = t6 - ModelPars[iM_Tc];
    result__[ 5   ] = -t31 * t25 * t1;
    result__[ 6   ] = t3 * (ModelPars[iM_Tf] - t6) + t23 - t31 * t27;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Drhs_odeDxup_sparse", 7, i_segment );
  }

