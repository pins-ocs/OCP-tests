  real_type
  ICLOCS_SingularArc::g_fun_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__
  ) const {
    integer i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_u];
    real_type t2   = uControl(t1, -2, 2);
    real_type t3   = P__[iP_T];
    real_type t4   = tfbound(-t3);
    real_type t10  = X__[iX_x1];
    real_type t11  = cos(t10);
    real_type t15  = sin(t10);
    real_type result__ = t1 * t3 * MU__[0] + t11 * t3 * MU__[1] + t15 * t3 * MU__[2] + t2 + t4;
    if ( m_debug ) {
      UTILS_ASSERT( Utils::is_finite(result__), "g_fun_eval(...) return {}\n", result__ );
    }
    return result__;
  }

