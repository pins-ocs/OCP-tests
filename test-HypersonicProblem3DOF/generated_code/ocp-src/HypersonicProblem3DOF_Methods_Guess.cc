  bool
  HypersonicProblem3DOF::penalties_check(
    NodeQX const & NODE__,
    P_const_p_type P__
  ) const {
    integer i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    bool ok = true;
    real_type t1   = X__[iX_G];
    ok = ok && G_bound_min.check_range(-0.314159265358979323846264338328e1 - t1);
    ok = ok && G_bound_max.check_range(t1 - 0.314159265358979323846264338328e1);
    return ok;
  }

