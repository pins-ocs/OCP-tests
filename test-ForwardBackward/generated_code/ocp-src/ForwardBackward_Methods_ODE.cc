  void
  ForwardBackward::rhs_ode_eval(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    Path2D::SegmentClass const & segment = pTrajectory->get_segment_by_index(i_segment);
    real_type t2   = X__[iX_v];
    result__[ 0   ] = 1.0 / t2 * U__[iU_a] - ModelPars[iM_c0] - t2 * ModelPars[iM_c1];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "rhs_ode", 1, i_segment );
  }

