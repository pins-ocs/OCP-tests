  real_type
  BangBangFmodule::g_fun_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__
  ) const {
    integer i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_Fp];
    real_type t2   = U__[iU_Fm];
    real_type t4   = controlP(t1, 0, ModelPars[iM_FpMax]);
    real_type t6   = controlM(t2, 0, ModelPars[iM_FmMax]);
    real_type result__ = t1 + t2 + t4 + t6 + MU__[0] * X__[iX_v] + (t1 - t2) * MU__[1];
    if ( m_debug ) {
      UTILS_ASSERT( Utils::is_finite(result__), "g_fun_eval(...) return {}\n", result__ );
    }
    return result__;
  }

