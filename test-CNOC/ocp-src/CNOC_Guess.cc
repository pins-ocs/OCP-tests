  void
  CNOC::xlambda_guess_eval(
    integer              i_segment,
    Q_const_pointer_type Q__,
    P_const_pointer_type P__,
    X_pointer_type       X__,
    L_pointer_type       L__
  ) const {
    ToolPath2D::SegmentClass const & segment = pToolPath2D->getSegmentByIndex(i_segment);
    X__[ iX_s   ] = Q__[0];
    X__[ iX_n   ] = 0;
    X__[ iX_vs  ] = ALIAS_nominalFeed();
    X__[ iX_vn  ] = 0;
    X__[ iX_as  ] = 0;
    X__[ iX_an  ] = 0;
    X__[ iX_coV ] = 1.0 / X__[2];

  }

