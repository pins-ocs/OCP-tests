  void
  farmer_problem::u_guess_eval(
    integer              i_segment,
    Q_const_pointer_type Q__,
    X_const_pointer_type X__,
    L_const_pointer_type L__,
    P_const_pointer_type P__,
    U_pointer_type       U__
  ) const {
    MeshStd::SegmentClass const & segment = pMesh->getSegmentByIndex(i_segment);
    std::fill_n( U__.pointer(), 4, 0 );

    U__[ iU_x1__o ] = 0;
    U__[ iU_x2__o ] = 0;
    U__[ iU_x3__o ] = 0;
    U__[ iU_x4__o ] = 0;
    #ifdef MECHATRONIX_DEBUG
    CHECK_NAN(U__.pointer(),"u_guess_eval",4);
    #endif
  }

