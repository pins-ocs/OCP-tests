  real_type
  Train::g_fun_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__
  ) const {
    integer i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_ua];
    real_type t2   = X__[iX_v];
    real_type t5   = uaControl(t1, 0, ModelPars[iM_uaMax]);
    real_type t6   = U__[iU_ub];
    real_type t8   = ubControl(t6, 0, ModelPars[iM_ubMax]);
    real_type t13  = acc(X__[iX_x], t2);
    real_type result__ = t2 * t1 + t5 + t8 + t2 * MU__[0] + (t13 + t1 - t6) * MU__[1];
    if ( m_debug ) {
      UTILS_ASSERT( Utils::is_finite(result__), "g_fun_eval(...) return {}\n", result__ );
    }
    return result__;
  }

