  integer Pugliese::boundaryConditions_numEqns() const { return 5; }

  void
  Pugliese::boundaryConditions_eval(
    NodeType const     & LEFT__,
    NodeType const     & RIGHT__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer i_segment_left  = LEFT__.i_segment;
    real_const_ptr     QL__ = LEFT__.q;
    real_const_ptr     XL__ = LEFT__.x;
    integer i_segment_right = RIGHT__.i_segment;
    real_const_ptr     QR__ = RIGHT__.q;
    real_const_ptr     XR__ = RIGHT__.x;
    MeshStd::SegmentClass const & segmentLeft  = pMesh->get_segment_by_index(i_segment_left);
    MeshStd::SegmentClass const & segmentRight = pMesh->get_segment_by_index(i_segment_right);
    result__[ 0   ] = XL__[iX_T] - ModelPars[iM_T0];
    result__[ 1   ] = XL__[iX_C] - ModelPars[iM_C0];
    result__[ 2   ] = XL__[iX_R] - ModelPars[iM_R0];
    result__[ 3   ] = XL__[iX_DD] - ModelPars[iM_D0];
    result__[ 4   ] = XL__[iX_I__p] - ModelPars[iM_Ip0];
    if ( m_debug )
      Mechatronix::check_in_segment2( result__, "boundaryConditions_eval", 5, i_segment_left, i_segment_right );
  }

