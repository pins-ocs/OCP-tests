  void
  SingularCalogeroModified::continuation_step_0( real_type s ) {
    real_type t1   = ModelPars[iM_epsilon];
    real_type t5   = log(1.0 / t1 * ModelPars[iM_epsilon_min]);
    real_type t7   = exp(t5 * s);
    ModelPars[iM_epsilon] = t7 * t1;
  }

