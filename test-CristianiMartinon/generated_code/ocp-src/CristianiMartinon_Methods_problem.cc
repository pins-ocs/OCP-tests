  real_type
  CristianiMartinon::H_eval(
    NodeQXL const & NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    real_const_ptr L__ = NODE__.lambda;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = P__[iP_T];
    real_type t3   = X__[iX_y];
    real_type t5   = yguess(Q__[iQ_zeta]);
    real_type t7   = pow(t3 - t5, 2);
    real_type t12  = cfun(X__[iX_x], t3);
    real_type t13  = U__[iU_u];
    real_type t14  = cos(t13);
    real_type t19  = sin(t13);
    real_type result__ = t14 * t12 * t1 * MU__[0] + t19 * t12 * t1 * MU__[1] + t7 * ModelPars[iM_WY] + t1;
    if ( m_debug ) {
      UTILS_ASSERT( Utils::is_finite(result__), "H_eval(...) return {}\n", result__ );
    }
    return result__;
  }

