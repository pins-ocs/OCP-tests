  void
  HangingChain::DgDu_sparse(
    NodeType2 const &    LEFT__,
    NodeType2 const &    RIGHT__,
    U_const_pointer_type UM__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer i_segment = LEFT__.i_segment;
    real_const_ptr QL__ = LEFT__.q;
    real_const_ptr XL__ = LEFT__.x;
    real_const_ptr LL__ = LEFT__.lambda;
    real_const_ptr QR__ = RIGHT__.q;
    real_const_ptr XR__ = RIGHT__.x;
    real_const_ptr LR__ = RIGHT__.lambda;
    // midpoint
    real_type QM__[1], XM__[2], LM__[2];
    // Qvars
    QM__[0] = (QL__[0]+QR__[0])/2;
    // Xvars
    XM__[0] = (XL__[0]+XR__[0])/2;
    XM__[1] = (XL__[1]+XR__[1])/2;
    // Lvars
    LM__[0] = (LL__[0]+LR__[0])/2;
    LM__[1] = (LL__[1]+LR__[1])/2;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = LM__[0];
    real_type t2   = UM__[0];
    real_type t3   = t2 * t2;
    real_type t4   = t3 + 1;
    real_type t5   = sqrt(t4);
    real_type t6   = 1.0 / t5;
    real_type t8   = t2 * t6 * t1;
    real_type t9   = LM__[1];
    real_type t10  = XL__[iX_x];
    real_type t13  = t5 * t1;
    real_type t14  = t2 * t9;
    real_type t18  = 1.0 / t5 / t4;
    real_type t21  = XR__[iX_x];
    result__[ 0   ] = t6 * (t8 + t9 + t10) - t2 * t18 * (t2 * t10 + t13 + t14) + t6 * (t8 + t9 + t21) - t2 * t18 * (t2 * t21 + t13 + t14);
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "DgDu_sparse", 1, i_segment );
  }

