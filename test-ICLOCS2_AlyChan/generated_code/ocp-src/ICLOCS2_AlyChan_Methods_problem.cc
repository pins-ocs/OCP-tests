  void
  ICLOCS2_AlyChan::continuation_step_0( real_type s ) {
    real_type t2   = 1 - s;
    uControl.update_epsilon(ModelPars[iM_u_epsi_min] * s + t2 * ModelPars[iM_u_epsi_max]);
    uControl.update_tolerance(ModelPars[iM_u_tol_min] * s + t2 * ModelPars[iM_u_tol_max]);
  }

