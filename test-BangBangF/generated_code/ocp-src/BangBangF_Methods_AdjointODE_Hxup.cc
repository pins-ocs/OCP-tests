  integer BangBangF::Hxp_numEqns() const { return 2; }

  void
  BangBangF::Hxp_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    result__[ 0   ] = 0;
    real_type t1   = X__[iX_v];
    real_type t2   = C1(t1);
    real_type t3   = ALIAS_C1_constr_D(t2);
    real_type t4   = C1_D(t1);
    result__[ 1   ] = t4 * t3 + MU__[0];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Hxp_eval", 2, i_segment );
  }


  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  integer BangBangF::DHxpDxpuv_numRows() const { return 2; }
  integer BangBangF::DHxpDxpuv_numCols() const { return 5; }
  integer BangBangF::DHxpDxpuv_nnz()     const { return 1; }

  void
  BangBangF::DHxpDxpuv_pattern( integer iIndex[], integer jIndex[] ) const {
    iIndex[0 ] = 1   ; jIndex[0 ] = 1   ;
  }


  void
  BangBangF::DHxpDxpuv_sparse(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    real_type t1   = X__[iX_v];
    real_type t2   = C1(t1);
    real_type t3   = ALIAS_C1_constr_DD(t2);
    real_type t4   = C1_D(t1);
    real_type t5   = t4 * t4;
    real_type t7   = ALIAS_C1_constr_D(t2);
    real_type t8   = C1_DD(t1);
    result__[ 0   ] = t5 * t3 + t8 * t7;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "DHxpDxpuv_sparse", 1, i_segment );
  }

