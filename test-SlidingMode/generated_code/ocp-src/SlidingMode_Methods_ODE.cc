  integer SlidingMode::rhs_ode_numEqns() const { return 2; }

  void
  SlidingMode::rhs_ode_eval(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    result__[ 0   ] = U__[iU_u];
    result__[ 1   ] = X__[iX_x] * X__[iX_x];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "rhs_ode", 2, i_segment );
  }

  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  integer SlidingMode::Drhs_odeDxup_numRows() const { return 2; }
  integer SlidingMode::Drhs_odeDxup_numCols() const { return 3; }
  integer SlidingMode::Drhs_odeDxup_nnz()     const { return 2; }

  void
  SlidingMode::Drhs_odeDxup_pattern( integer iIndex[], integer jIndex[] ) const {
    iIndex[0 ] = 0   ; jIndex[0 ] = 2   ;
    iIndex[1 ] = 1   ; jIndex[1 ] = 0   ;
  }


