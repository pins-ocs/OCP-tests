  integer vanDerPol::Hxp_numEqns() const { return 2; }

  void
  vanDerPol::Hxp_eval(
    NodeType2 const    & NODE__,
    V_const_pointer_type V__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    real_const_ptr L__ = NODE__.lambda;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = X__[iX_x1];
    real_type t3   = L__[iL_lambda2__xo];
    real_type t4   = X__[iX_x2];
    result__[ 0   ] = 2 * t1 + (-2 * t4 * t1 - 1) * t3;
    real_type t11  = t1 * t1;
    result__[ 1   ] = 2 * t4 + L__[iL_lambda1__xo] + (-t11 + 1) * t3;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Hxp_eval", 2, i_segment );
  }


  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  integer vanDerPol::DHxpDxpu_numRows() const { return 2; }
  integer vanDerPol::DHxpDxpu_numCols() const { return 3; }
  integer vanDerPol::DHxpDxpu_nnz()     const { return 4; }

  void
  vanDerPol::DHxpDxpu_pattern( integer iIndex[], integer jIndex[] ) const {
    iIndex[0 ] = 0   ; jIndex[0 ] = 0   ;
    iIndex[1 ] = 0   ; jIndex[1 ] = 1   ;
    iIndex[2 ] = 1   ; jIndex[2 ] = 0   ;
    iIndex[3 ] = 1   ; jIndex[3 ] = 1   ;
  }


  void
  vanDerPol::DHxpDxpu_sparse(
    NodeType2 const    & NODE__,
    V_const_pointer_type V__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    real_const_ptr L__ = NODE__.lambda;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = L__[iL_lambda2__xo];
    result__[ 0   ] = -2 * X__[iX_x2] * t1 + 2;
    result__[ 1   ] = -2 * X__[iX_x1] * t1;
    result__[ 2   ] = result__[1];
    result__[ 3   ] = 2;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "DHxpDxpu_sparse", 4, i_segment );
  }

