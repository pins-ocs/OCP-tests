  integer LUUS_Singular04::Hxp_numEqns() const { return 3; }

  void
  LUUS_Singular04::Hxp_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    real_type t1   = X__[iX_x1];
    real_type t3   = uControl(U__[iU_u], -1, 1);
    result__[ 0   ] = 2 * t3 * t1 + 2 * t1;
    result__[ 1   ] = MU__[0];
    result__[ 2   ] = MU__[1];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Hxp_eval", 3, i_segment );
  }


  // - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
  integer LUUS_Singular04::DHxpDxpuv_numRows() const { return 3; }
  integer LUUS_Singular04::DHxpDxpuv_numCols() const { return 7; }
  integer LUUS_Singular04::DHxpDxpuv_nnz()     const { return 2; }

  void
  LUUS_Singular04::DHxpDxpuv_pattern( integer iIndex[], integer jIndex[] ) const {
    iIndex[0 ] = 0   ; jIndex[0 ] = 0   ;
    iIndex[1 ] = 0   ; jIndex[1 ] = 3   ;
  }


  void
  LUUS_Singular04::DHxpDxpuv_sparse(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = m_pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_u];
    real_type t2   = uControl(t1, -1, 1);
    result__[ 0   ] = 2 + 2 * t2;
    real_type t5   = ALIAS_uControl_D_1(t1, -1, 1);
    result__[ 1   ] = 2 * t5 * X__[iX_x1];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "DHxpDxpuv_sparse", 2, i_segment );
  }

