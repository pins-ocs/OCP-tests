  integer BangBangFredundant::Hxp_numEqns() const { return 6; }

  void
  BangBangFredundant::Hxp_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    result__[ 0   ] = 0;
    result__[ 1   ] = MU__[0];
    real_type t1   = ModelPars[iM_w_F];
    real_type t2   = X__[iX_F1];
    real_type t3   = X__[iX_F2];
    real_type t5   = ALIAS_Flim_min_D(-1 - t2 - t3);
    real_type t8   = ALIAS_Flim_max_D(t2 + t3 - 1);
    result__[ 2   ] = -t5 * t1 + t8 * t1 + MU__[1];
    result__[ 3   ] = result__[2];
    result__[ 4   ] = MU__[2];
    result__[ 5   ] = MU__[3];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Hxp_eval", 6, i_segment );
  }


