  void
  GoddardRocket::Drhs_odeDxpu_sparse(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    result__[ 0   ] = P__[iP_TimeSize];
    result__[ 1   ] = X__[iX_v];
    real_type t1   = X__[iX_h];
    real_type t2   = DD_D_1(t1, result__[1]);
    real_type t4   = X__[iX_m];
    real_type t5   = 1.0 / t4;
    result__[ 2   ] = -t5 * t2 * result__[0];
    real_type t7   = DD_D_2(t1, result__[1]);
    result__[ 3   ] = -t5 * t7 * result__[0];
    real_type t10  = U__[iU_u];
    real_type t11  = T(t10);
    real_type t12  = DD(t1, result__[1]);
    real_type t13  = t11 - t12;
    real_type t15  = t4 * t4;
    result__[ 4   ] = -1.0 / t15 * t13 * result__[0];
    result__[ 5   ] = t5 * t13 - ModelPars[iM_g];
    real_type t20  = T_D(t10);
    real_type t21  = t20 * result__[0];
    result__[ 6   ] = t5 * t21;
    real_type t23  = 1.0 / ModelPars[iM_Ve];
    result__[ 7   ] = -t23 * t11;
    result__[ 8   ] = -t23 * t21;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Drhs_odeDxpu_sparse", 9, i_segment );
  }

