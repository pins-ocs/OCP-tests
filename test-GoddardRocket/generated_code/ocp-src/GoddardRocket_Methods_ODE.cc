  void
  GoddardRocket::rhs_ode_eval(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = P__[iP_TimeSize];
    real_type t2   = X__[iX_v];
    result__[ 0   ] = t2 * t1;
    real_type t4   = T(U__[iU_u]);
    real_type t6   = DD(X__[iX_h], t2);
    result__[ 1   ] = (1.0 / X__[iX_m] * (t4 - t6) - ModelPars[iM_g]) * t1;
    result__[ 2   ] = -1.0 / ModelPars[iM_Ve] * t4 * t1;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "rhs_ode", 3, i_segment );
  }

