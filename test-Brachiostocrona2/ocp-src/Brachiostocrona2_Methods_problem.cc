    result__[ 1   ] = XR__[iX_y] - XL__[iX_y];
    result__[ 2   ] = XR__[iX_v] - XL__[iX_v];
    result__[ 3   ] = LR__[iL_lambda1__xo] - LL__[iL_lambda1__xo];
    result__[ 4   ] = LR__[iL_lambda2__xo] - LL__[iL_lambda2__xo];
