  void
  FlowInAchannel::Drhs_odeDxpu_sparse(
    NodeType const     & NODE__,
    U_const_pointer_type U__,
    P_const_pointer_type P__,
    real_type            result__[]
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    result__[ 0   ] = 1;
    result__[ 1   ] = 1;
    result__[ 2   ] = 1;
    real_type t1   = ModelPars[iM_R];
    result__[ 3   ] = -X__[iX_u3] * t1;
    result__[ 4   ] = X__[iX_u2] * t1;
    result__[ 5   ] = X__[iX_u1] * t1;
    result__[ 6   ] = -X__[iX_u] * t1;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Drhs_odeDxpu_sparse", 7, i_segment );
  }

