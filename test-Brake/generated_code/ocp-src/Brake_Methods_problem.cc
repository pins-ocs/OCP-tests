    real_type t12  = guess_u(Q__[iQ_zeta]);
    real_type t14  = pow(t4 - t12, 2);
    real_type result__ = -t8 * ModelPars[iM_epsilon] * P__[iP_T] + t14 * ModelPars[iM_mu];
