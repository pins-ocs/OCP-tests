  integer ICLOCS_Catalyst::Hxp_numEqns() const { return 2; }

  void
  ICLOCS_Catalyst::Hxp_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__,
    V_const_p_type  V__,
    real_ptr        result__
  ) const {
    integer i_segment  = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t2   = U__[iU_u];
    real_type t3   = t2 * MU__[0];
    real_type t4   = MU__[1];
    result__[ 0   ] = t2 * t4 - t3;
    result__[ 1   ] = 10 * t3 + (-9 * t2 - 1) * t4;
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "Hxp_eval", 2, i_segment );
  }


