  integer ICLOCS_Catalyst::ode_numEqns() const { return 2; }

  void
  ICLOCS_Catalyst::ode_eval(
    NodeQX const & NODE__,
    P_const_p_type P__,
    U_const_p_type U__,
    V_const_p_type V__,
    real_ptr       result__
  ) const {
    integer  i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_u];
    real_type t2   = X__[iX_x2];
    real_type t5   = 10 * t2 - X__[iX_x1];
    result__[ 0   ] = t5 * t1 - V__[0];
    result__[ 1   ] = -t5 * t1 - t2 * (1 - t1) - V__[1];
    if ( m_debug )
      Mechatronix::check_in_segment( result__, "ode", 2, i_segment );
  }

