  real_type
  TwoPhaseSchwartz::H_eval(
    integer              i_segment,
    CellType const &     CELL__,
    P_const_pointer_type P__
  ) const {
    integer        i_cell = CELL__.i_cell;
    real_type const * Q__ = CELL__.qM;
    real_type const * X__ = CELL__.xM;
    real_type const * L__ = CELL__.lambdaM;
    real_type const * U__ = CELL__.uM;
    MeshStd::SegmentClass const & segment = pMesh->getSegmentByIndex(i_segment);
    real_type t1   = X__[0];
    real_type t3   = pow(t1 - 1, 2);
    real_type t5   = X__[1];
    real_type t8   = pow(0.3333333333e1 * t5 - 0.1333333333e1, 2);
    real_type t10  = bound1(-1 + 9 * t3 + t8);
    real_type t12  = bound2(0.8e0 + t5);
    real_type t14  = U__[1];
    real_type t15  = t14 * t14;
    real_type t21  = t1 * t1;
    real_type t29  = ModelPars[0];
    real_type t31  = X__[3];
    real_type t36  = X__[2] * X__[2];
    return t10 + t12 + t15 * ModelPars[3] + t5 * L__[0] + (U__[0] - 0.1e0 * t5 * (2 * t21 + 1)) * L__[1] + t31 * t29 * L__[2] + (t14 - 0.1e0 * t31 * (2 * t36 + 1)) * t29 * L__[3];
  }

