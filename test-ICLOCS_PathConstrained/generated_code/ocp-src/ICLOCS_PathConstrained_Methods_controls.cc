  real_type
  ICLOCS_PathConstrained::g_fun_eval(
    NodeQX const &  NODE__,
    P_const_p_type  P__,
    MU_const_p_type MU__,
    U_const_p_type  U__
  ) const {
    integer i_segment = NODE__.i_segment;
    real_const_ptr Q__ = NODE__.q;
    real_const_ptr X__ = NODE__.x;
    MeshStd::SegmentClass const & segment = pMesh->get_segment_by_index(i_segment);
    real_type t1   = U__[iU_u];
    real_type t2   = t1 * t1;
    real_type t5   = X__[iX_x1] * X__[iX_x1];
    real_type t6   = X__[iX_x2];
    real_type t7   = t6 * t6;
    real_type t8   = uControl(t1, -20, 20);
    real_type t11  = pow(Q__[iQ_zeta] - 0.5e0, 2);
    real_type t14  = x2bound(0.5e0 - 8 * t11 + t6);
    real_type result__ = 0.5e-2 * t2 + t5 + t7 + t8 + t14 + t6 * MU__[0] + (-t6 + t1) * MU__[1];
    if ( m_debug ) {
      UTILS_ASSERT( Utils::is_finite(result__), "g_fun_eval(...) return {}\n", result__ );
    }
    return result__;
  }

